import { ParsingError } from "@js-soft/ts-serval";
import { AffiliationOrganization, Consent, GivenName } from "../../src";

describe("Test valid Attributes", () => {
    test("GivenName is considered as valid", () => {
        const validGivenName = GivenName.from({ value: "Gräf" });
        expect(validGivenName.value.toString()).toBe("Gräf");
    });

    test("AffiliationOrganization is considered as valid", () => {
        const validOrganization = AffiliationOrganization.from({ value: "€" });
        expect(validOrganization.value.toString()).toBe("€");
    });

    test("Consent is considered as valid", () => {
        const consent = Consent.from({ consent: "\u0009" });
        expect(consent.consent.toString()).toBe("\u0009");
    });
});

